package charecterrr.liab;

public class Mymain {

	public static void main(String[] args) {
		///   return always boolean   ////////
		
		/////////////////   isletter    ///////////////////////	
		System.out.print(Character.isLetter('C'));
		System.out.print("      ISLETTER");
		System.out.println();System.out.println();
		
		
		
		/////////////////   isdigit    ///////////////////////	
		System.out.print(Character.isDigit('C'));
		System.out.print("      ISDIGIT");
		System.out.println();System.out.println();
		
		
		
		/////////////////   isWhitespace   ///////////////////////	
		System.out.print(Character.isWhitespace('\t'));
		System.out.print("      ISWHITESPACE");
		System.out.println();System.out.println();
		
		
		
		/////////////////   isUpperCase   ///////////////////////	
		System.out.print(Character.isUpperCase('S'));
		System.out.print("      ISUPPERCASE");
		System.out.println();System.out.println();
		
		
		
		/////////////////   isUpperCase   ///////////////////////	
		System.out.print(Character.isLowerCase('S'));
		System.out.print("      ISlowercase");
		System.out.println();System.out.println();
		
		
		
		/////////////////   toUpperCase   ///////////////////////	
		System.out.print(Character.toLowerCase('S'));
		System.out.print("      tolowercase");
		System.out.println();System.out.println();
		
		
		
		/////////////////   toUpperCase   ///////////////////////	
		System.out.print(Character.toUpperCase('a'));
		System.out.print("      TOUPPERCASSE");
		System.out.println();System.out.println();

		
		
		/////////////////   toSTRING ///////////////////////	
		String ss=Character.toString('a');
		System.out.print(ss);
		System.out.print("      TOSTRING");
		System.out.println();System.out.println();

		
		
		/////////////////   END       ///////////////////////
	}

}
